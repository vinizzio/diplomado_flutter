void main() {
  // String
  String saludo = "Hola mundo";  
  print(saludo); // print("Hola mundo");

  // Números
  // Entero - int 
  // Decimal - double
  int edad = 30;
  double precio = 12.50;
  print(edad);
  print(precio);

  // Booleano - bool
  bool esVerdad = false;
  print(esVerdad);

  // Hola mi nombre es Richar y tengo 29 años
  String nombre = "Richar";
  print("Hola mi nombre es $nombre y tengo $edad años");


  // Variables, Finales, Constantes

  // Tipo de dato
  String apellido = "Cangui";
  apellido = "Cangui Laica";

  // var - Variable
  var fruta = "Manzana";
  print(fruta);
  fruta = "Nueva manzana";
  print(fruta);

  // final 
  final precioFinal = 10.25;
  // precioFinal = 11.00; NO SE PUEDE HACER
  print(precioFinal);

  // const
  const pi = 3.1415;
  // pi = 3.1416; NO SE PUEDE HACER
  print(pi);
}
