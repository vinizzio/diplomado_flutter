void main() {
  int a = 10;
  int b = 3;
  /// Suma +
  print("Suma: ${a + b}");
  final suma = a + b;
  print("Suma: $suma");
  /// Resta -
  print("Resta: ${a - b}");
  final resta = a - b;
  print("Resta: $resta");
  /// Multiplicación *
  print("Multiplicación: ${a * b}");
  final multiplica = a * b;
  print("Multiplicación: $multiplica");
  /// División /
  print("División: ${a / b}");
  final divi = a / b;
  print("División: $divi");

  // División entera
  // alt + 126
  print("División entera: ${a ~/ b}");
  
  // Módulo
  print("Módulo: ${a % b}");
  /// 10  |   3
  ///  9      1
  ///  1
  
}