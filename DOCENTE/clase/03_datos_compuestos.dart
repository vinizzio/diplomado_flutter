void main() {
  // List
  // List, var, final, const
  // Una lista es como una cesta, y va a tener cosas internamente
  // Tipo de dato que va a contener la lista
  // alt+60 >
  // alt+62 <
  List<String> listaDeCompras = ["Pera", "Manzana", "Uva"];
