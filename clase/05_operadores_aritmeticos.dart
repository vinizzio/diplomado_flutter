void main (){
  int a= 10;
  int b = 3;
  ///suma
  print("suma: ${a+b}");
  final suma = a + b;
  print("suma: $suma");
  ///resta
  print("resta: ${a-b}");
  final resta = a - b;
  print("resta: $resta");
  ///mutiplicacion
  print("multiplicacion: ${a*b}");
  final multi = a * b;
  print("multiplicacion: $multi");  
  ///division
  print("division: ${a/b}");
  final division = a / b;
  print("division: $division");
  ///division entera
  print("divisionent: ${a~/b}");
  final divisionent = a ~/ b;
  print("divisionent: $divisionent");
  ///modulo
  print("divisionent: ${a%b}");
}